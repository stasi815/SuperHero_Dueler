class Dog:
    # Required properties are defined inside the __init__ constructor method
    def __init__(self, name, breed):
        self.name = name
        self.breed = breed
    # Methods are defined as their own named functions inside the class
    def bark(self):
        print("woof!")
    def sit(self):
        print("dog sits")
    def roll_over(self):
        print("doggy rolled")

    

